// EXERCISE 01 Arithmetic operators
let x=7,y=3;
console.log(`Addition operation x+y=10`)
console.log(x+y)

console.log(`Subtraction operation x-y=4`)
console.log(x-y)

console.log(`Multiplication operation x*y=21`)
console.log(x*y)

console.log(`Division operation x/y=2.333`)
console.log(x/y)

console.log(`Reminder operation x%y=1`)
console.log(x%y)

console.log(`Post-increment operation x++`)
console.log(x++)
console.log(x)

console.log(`Pre-increment operation x++`)
console.log(++x)
